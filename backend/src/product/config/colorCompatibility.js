/**
 * Default score when a color pair is not found in the matrix.
 */
const DEFAULT_COLOR_SCORE = 50;

/**
 * Look up the compatibility score between two colors.
 * Case-insensitive. Returns DEFAULT_COLOR_SCORE if pair not found.
 *
 * @param {string} colorA
 * @param {string} colorB
 * @returns {number} 0-100
 */
export function getColorScore(colorA, colorB) {
  if (!colorA || !colorB) return 0;

  const a = colorA.toLowerCase().trim();
  const b = colorB.toLowerCase().trim();

  // Check a→b first, then b→a (matrix is mostly symmetric but defensive)
  return colorMatrix[a]?.[b]
    ?? colorMatrix[b]?.[a]
    ?? DEFAULT_COLOR_SCORE;
}

export default colorMatrix;
